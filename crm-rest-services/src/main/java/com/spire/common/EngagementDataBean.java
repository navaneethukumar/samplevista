package com.spire.common;

/**
 * Data setting for Engagement Score batch
 * 
 * @author Pradeep
 *
 */
public class EngagementDataBean {
	int benefitLevel;
	int companyLevel;
	int interestLevel;
	int fitmentLevel;

	public int getBenefitLevel() {
		return benefitLevel;
	}

	public void setBenefitLevel(int benefitLevel) {
		this.benefitLevel = benefitLevel;
	}

	public int getCompanyLevel() {
		return companyLevel;
	}

	public void setCompanyLevel(int companyLevel) {
		this.companyLevel = companyLevel;
	}

	public int getInterestLevel() {
		return interestLevel;
	}

	public void setInterestLevel(int interestLevel) {
		this.interestLevel = interestLevel;
	}

	public int getFitmentLevel() {
		return fitmentLevel;
	}

	public void setFitmentLevel(int fitmentLevel) {
		this.fitmentLevel = fitmentLevel;
	}

}
